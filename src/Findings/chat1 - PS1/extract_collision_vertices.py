def find_blob_in_dump(dump: bytes, cfg: dict) -> int:
    """
    Locate the level blob inside an RDRAM dump.

    Searches for the level's self-pointer (w0) and validates the hit by
    checking that n_vtx and n_face fields match the expected values.
    Works regardless of emulator (BizHawk, Project64, Mupen64Plus standalone).

    Returns the byte offset of the blob start within the dump, or -1.
    """
    target    = struct.pack('>I', cfg["self_ptr"])
    n_vtx_exp = cfg["n_vtx"]
    n_face_exp = cfg["n_faces"]
    n_vtx_off  = cfg["desc_n_vtx_off"]
    n_face_off = cfg["desc_n_face_off"]

    pos = 0
    while True:
        idx = dump.find(target, pos)
        if idx < 0:
            break
        # Validate descriptor fields
        if idx + n_vtx_off + 8 <= len(dump):
            n_vtx  = struct.unpack_from('>I', dump, idx + n_vtx_off)[0]
            n_face = struct.unpack_from('>I', dump, idx + n_face_off)[0]
            if n_vtx == n_vtx_exp and n_face == n_face_exp:
                return idx
        pos = idx + 4
    return -1


def find_vtx_buffer(dump: bytes, blob_base: int, cfg: dict) -> int:
    """
    Locate the runtime vertex buffer (seg1) in the dump.

    Scans for the first search anchor at 8B stride alignment, then validates
    with additional anchors. Returns dump offset of vtx[0], or -1.
    """
    probes  = cfg["search_anchors"]
    stride  = cfg["vtx_stride"]   # 8B: [X:i16][Y:i16][Z:i16][flags:u16]
    n_vtx   = cfg["n_vtx"]

    ax0, ay0, az0 = probes[0]
    ax1, ay1, az1 = probes[1]
    pat0 = struct.pack('>hhh', ax0, ay0, az0)
    pat1 = struct.pack('>hhh', ax1, ay1, az1)

    # Search the full dump (the vertex buffer may be outside the blob region)
    for off in range(0, len(dump) - n_vtx * stride, stride):
        if dump[off:off+6] != pat0:
            continue
        # Check vtx[3] = anchor for tile 1 (stride * 3 bytes away)
        if dump[off + stride*3 : off + stride*3 + 6] != pat1:
            continue
        # Validate with more probes
        ok = 0
        for pi, (px, py, pz) in enumerate(probes[2:6], start=2):
            pat = struct.pack('>hhh', px, py, pz)
            if dump[off + stride*pi : off + stride*pi + 6] == pat:
                ok += 1
        if ok >= 2:
            return off
    return -1


def extract_vtx_positions(dump: bytes, vtx_base: int, cfg: dict) -> list:
    """Read all n_vtx positions from the runtime vertex buffer."""
    n_vtx  = cfg["n_vtx"]
    stride = cfg["vtx_stride"]
    vtx    = []
    for i in range(n_vtx):
        off = vtx_base + i * stride
        x, y, z = struct.unpack_from('>hhh', dump, off)
        vtx.append((x, y, z))
    return vtx


def assess_vtx_quality(vtx: list, cfg: dict) -> dict:
    """
    Check how complete the vertex buffer is.
    Corner 0 of each tile is always populated (it's the anchor).
    Corners 1 and 2 need at least one frame to have rendered.
    Returns a dict with counts and a verdict.
    """
    SENTINEL_X = [1, -32719]   # values seen in unpopulated corner 1/2 slots
    n_vtx      = len(vtx)
    populated  = 0
    for i, (x, y, z) in enumerate(vtx):
        corner = i % 3
        if corner == 0:
            populated += 1   # corner 0 always valid
        elif x not in SENTINEL_X:
            populated += 1

    pct = populated / n_vtx * 100
    complete = pct > 95.0
    return {"populated": populated, "total": n_vtx, "pct": pct, "complete": complete}

