def decompress_level(rom: bytes, level_name: str) -> bytes:
    idx = LEVEL_IDX_ROM
    for i in range(40):
        off = idx + i * 32
        name = rom[off+20:off+32].rstrip(b'\x00').decode('ascii', 'replace')
        if name == level_name:
            rs, re = struct.unpack_from('>II', rom, off)
            data = bytes(rom[rs:re])
            for strip in range(17):
                try:
                    return zlib.decompressobj(-15).decompress(
                        data[:-strip] if strip else data)
                except Exception:
                    pass
    raise ValueError(f"Level '{level_name}' not found in ROM index")


def extract_static_anchors(blob: bytes, cfg: dict) -> list:
    anchors = []
    base = cfg["batch_off"]
    for i in range(cfg["n_batch"]):
        off = base + i * 24
        x = struct.unpack_from('>h', blob, off + 8)[0]
        y = struct.unpack_from('>h', blob, off + 10)[0]
        z = struct.unpack_from('>h', blob, off + 12)[0]
        anchors.append((x, y, z))
    return anchors


def read_face_triangles(blob: bytes, cfg: dict) -> list:
    tris   = []
    n_vtx  = cfg["n_vtx"]
    stride = cfg["face_stride"]
    base   = cfg["face_table_off"]
    n_face = cfg["n_faces"]
    for i in range(n_face):
        off = base + i * stride
        if off + stride > len(blob):
            break
        if stride == 16:
            v0, v1, v2 = struct.unpack_from('>HHH', blob, off + 4)
            flags       = struct.unpack_from('>H',   blob, off + 10)[0]
            if all(v < n_vtx for v in (v0, v1, v2)):
                tris.append((v0, v1, v2, flags))
        else:
            vA0, vA1, vA2 = struct.unpack_from('>HHH', blob, off + 4)
            flagsA         = struct.unpack_from('>H',   blob, off + 10)[0]
            vB0, vB1, vB2 = struct.unpack_from('>HHH', blob, off + 20)
            flagsB         = struct.unpack_from('>H',   blob, off + 26)[0]
            if all(v < n_vtx for v in (vA0, vA1, vA2)):
                tris.append((vA0, vA1, vA2, flagsA))
            if vB0 != NULL_VTX and all(v < n_vtx for v in (vB0, vB1, vB2)):
                tris.append((vB0, vB1, vB2, flagsB))
    return tris

