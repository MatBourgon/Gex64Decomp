def write_vtx_c(vtx: list, level_name: str, suffix: str) -> str:
    filename = f"{level_name}_collision_vtx{suffix}.c"
    macro    = f"{level_name.upper()}_N_COLLISION_VTX"
    with open(filename, 'w') as f:
        f.write(f"/* Gex 64 — {level_name} collision vertices */\n\n")
        f.write(f"#include <stdint.h>\n\n")
        f.write(f"#define {macro} {len(vtx)}\n\n")
        f.write(f"typedef struct {{ int16_t x, y, z; }} CollVtx;\n\n")
        f.write(f"CollVtx {level_name}_collision_vtx[{macro}] = {{\n")
        for i, (x, y, z) in enumerate(vtx):
            comma = ',' if i < len(vtx) - 1 else ''
            if i % 3 == 0:
                f.write(f"    /* tile {i // 3} */\n")
            f.write(f"    {{{x:7},{y:7},{z:7}}}{comma}\n")
        f.write("};\n")
    return filename


def write_face_c(tris: list, level_name: str) -> str:
    filename = f"{level_name}_collision_faces.c"
    macro    = f"{level_name.upper()}_N_COLLISION_FACES"
    with open(filename, 'w') as f:
        f.write(f"/* Gex 64 — {level_name} collision faces */\n\n")
        f.write(f"#include <stdint.h>\n\n")
        f.write(f"#define {macro} {len(tris)}\n\n")
        f.write(f"typedef struct {{ uint16_t v0, v1, v2, flags; }} CollFace;\n\n")
        f.write(f"CollFace {level_name}_collision_faces[{macro}] = {{\n")
        for i, (v0, v1, v2, flags) in enumerate(tris):
            comma = ',' if i < len(tris) - 1 else ''
            f.write(f"    {{{v0},{v1},{v2},0x{flags:04X}}}{comma}\n")
        f.write("};\n")
    return filename

